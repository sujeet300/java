package control;

public class Elseif
{
	public static void main(String[] args) 
	{
		int i=70;
			
			if(i>=65)
			{
				
				System.out.println("distinction");
			}
			else if(i>=60)
				
			{
				
				System.out.println("first class");
				
			}
		
			else if(i>=35)
			{
				
				System.out.println("pass");
			}
		
			else
			{
				System.out.println("fail");
				
			}
		
	}

	
	
}
